# Kullanıcıdan iki değer alma
num1 = float(input("Birinci sayıyı girin: "))
num2 = float(input("İkinci sayıyı girin: "))

# Aritmetik işlemler
toplam = num1 + num2
fark = num1 - num2
carpma = num1 * num2
bolme = num1 / num2

# Sonuçları yazdırma
print(f"Toplam: {toplam}")
print(f"Fark: {fark}")
print(f"Çarpma: {carpma}")
print(f"Bölme: {bolme}")
